using System;

namespace Pemrog_Visual_2.BAB4
{
    class For1
    {
        public static void Main()
        {
            for (int i = 0; i < 10; i++)
            {
                if (i % 2 == 0)
                {
                    Console.WriteLine(i);
                }
            }
            Console.ReadLine();
        }
    }
}
